def 民国和公元转换(年份,转换模式):
    if 转换模式=="公元纪年":
        民国年份=1912+年份-1
        if 年份==1:
            print("民国元年是公元",民国年份,"年。")
        else:
            print("民国",年份,"是公元",民国年份,"年。")
    elif 转换模式=="民国纪年":
        公元年份=年份-1912
        if 公元年份==0:
            print("公元",年份,"是民国元年。")
        else:
            print("公元",年份,"是民国",年份-1912,"年。")
    else:
        print("错误。")